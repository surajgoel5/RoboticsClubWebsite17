                                Magneto, a Hand Gesture controlled bot to complete the problem statement which consisted of traversing the path and moving and placing of blocks and cubes over a certain distance by overcoming obstacles in between. The bot was required to empty a tumbler into another one.
The bot designed by the team was controlled by accelerometers, and the data was transmitted via bluetooth modules to the bot.<br><br>
The controller consisted of Arduino uno while the bot was run by Arduino Mega.
The bot used a differential drive and consisted of gripping with three degrees of freedom (gripping arm, rolling action, picking action).
The designed bot was successfully able to grab boxes, roll and pick them. However due to heavy weight of arm the control wasn't smooth. Also the bot had trouble climbing on slopes due to unbalanced torque of arm.
                    <br><br>
							This was made to compete in the Magneto competetion of Shaastra 2016(IIT Madras). Two teams won 1st and 3rd Prize.

					
